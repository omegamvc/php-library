declare(strict_types=1);

namespace Omega\Http\Exceptions;

use Exception;

/**
 * Class StreamedResponseCallableException
 *
 * Thrown when a streamed response is attempted without a valid callback function.
 * Streamed responses require a callable to generate output during the response phase.
 *
 * @category   Omega
 * @package    Http
 * @subpackage Exceptions
 * @link       https://omegamvc.github.io
 * @author     Adriano Giovannini <agisoftt@gmail.com>
 * @copyright  Copyright (c) 2024 - 2025 Adriano Giovannini (https://omegamvc.github.io)
 * @license    https://www.gnu.org/licenses/gpl-3.0-standalone.html     GPL V3.0+
 * @version    2.0.0
 */
class StreamedResponseCallableException extends Exception implements HttpExceptionInterface
{
    /**
     * Creates a new exception instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct('Stream callback must not be null');
    }
}
